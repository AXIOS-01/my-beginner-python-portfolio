sentance = ("The!quick!brown!fox!jumps!over!the!lazy!dog.") # stores the sentance in the variable "sentance"
print (sentance.replace("!", " ")) #this replaces each ! with a space
print (sentance.upper().replace("!", " ")) #this replaces each ! with a space and makes each character uppercase
print (sentance.upper().replace("!", " ")[::-1]) #this replaces each ! with a space and makes each character uppercase and reverses the order of each character

